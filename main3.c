int main3(void)
{

int n , valeurChiffre = 0;
printf("Valeur de N?");
scanf("%d", &n);


if (n = 0) {

    valeurChiffre;


if (n > 0) {

    valeurChiffre++;
};

if (n < 0){

    valeurChiffre--;
}


    return 0;
}

printf("La valeur chiffre est : %d\n", valeurChiffre);

return 0;
};
